def normalize_linkedin(text):
    raw = text.strip()
    if raw.lower() in {"нет", "no", "-", "none"}:
        return "Нет"
    if "linkedin.com" in raw.lower():
        if raw.startswith("http://") or raw.startswith("https://"):
            return raw
        return "https://" + raw
    return None


