async def handle_answer(query, question_index, selected_index, context):
    session = get_session(query.from_user.id)

    if not session or session["state"] != "testing":
        await query.answer("Сессия теста не найдена. Нажмите /start.", show_alert=True)
        return

    if question_index != session["current_index"]:
        await query.answer("Этот вопрос уже обработан.", show_alert=False)
        return

    question = get_question(session, question_index)
    answers = list(session["answers"])
    answers.append(selected_index)

    score = session["score"]
    if selected_index == question["correct"]:
        score += 1

    next_index = question_index + 1

    if next_index >= TEST_SIZE:
        update_session_from_user(
            query.from_user,
            state="awaiting_full_name",
            current_index=next_index,
            answers=answers,
            score=score,
        )
        save_action(query.from_user, f"Завершил вопросы: {PROFESSIONS[session['profession']]['title']} | {score}/{TEST_SIZE}")

        await safe_edit_message(
            query.message,
            f"✅ Вы ответили на все вопросы.\n\nВаш результат: {score}/{TEST_SIZE}\n\n{ASK_FULL_NAME_TEXT}"
        )
        return

    update_session_from_user(
        query.from_user,
        current_index=next_index,
        answers=answers,
        score=score,
    )

    new_session = get_session(query.from_user.id)
    await safe_edit_message(
        query.message,
        build_question_text(new_session),
        reply_markup=build_question_keyboard(new_session)
    )


async def handle_go_back(query):
    session = get_session(query.from_user.id)

    if not session or session["state"] != "testing":
        await query.answer("Назад вернуться нельзя.", show_alert=False)
        return

    if session["current_index"] <= 0 or not session["answers"]:
        await query.answer("Назад вернуться нельзя.", show_alert=False)
        return

    previous_index = session["current_index"] - 1
    previous_question = get_question(session, previous_index)

    answers = list(session["answers"])
    last_selected = answers.pop()

    score = session["score"]
    if last_selected == previous_question["correct"]:
        score -= 1

    update_session_from_user(
        query.from_user,
        current_index=previous_index,
        answers=answers,
        score=score,
    )

    new_session = get_session(query.from_user.id)
    save_action(query.from_user, f"Вернулся на вопрос {previous_index + 1}")

    await safe_edit_message(
        query.message,
        build_question_text(new_session),
        reply_markup=build_question_keyboard(new_session)
    )


