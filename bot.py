def get_session(user_id):
    conn = get_conn()
    cur = conn.cursor()
    cur.execute("SELECT * FROM user_sessions WHERE user_id = ?", (user_id,))
    row = cur.fetchone()
    conn.close()

    if not row:
        return None

    return {
        "user_id": row["user_id"],
        "username": row["username"],
        "first_name": row["first_name"],
        "profession": row["profession"],
        "state": row["state"],
        "current_index": row["current_index"],
        "question_ids": json.loads(row["question_ids_json"] or "[]"),
        "answers": json.loads(row["answers_json"] or "[]"),
        "score": row["score"],
        "full_name": row["full_name"],
        "linkedin_url": row["linkedin_url"],
        "updated_at": row["updated_at"],
    }


def save_session_data(session):
    conn = get_conn()
    cur = conn.cursor()
    cur.execute("""
        INSERT INTO user_sessions (
            user_id, username, first_name, profession, state, current_index,
            question_ids_json, answers_json, score, full_name, linkedin_url, updated_at
        )
        VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
        ON CONFLICT(user_id) DO UPDATE SET
            username=excluded.username,
            first_name=excluded.first_name,
            profession=excluded.profession,
            state=excluded.state,
            current_index=excluded.current_index,
            question_ids_json=excluded.question_ids_json,
            answers_json=excluded.answers_json,
            score=excluded.score,
            full_name=excluded.full_name,
            linkedin_url=excluded.linkedin_url,
            updated_at=excluded.updated_at
    """, (
        session["user_id"],
        session.get("username"),
        session.get("first_name"),
        session.get("profession"),
        session["state"],
        session["current_index"],
        json.dumps(session.get("question_ids", []), ensure_ascii=False),
        json.dumps(session.get("answers", []), ensure_ascii=False),
        session.get("score", 0),
        session.get("full_name"),
        session.get("linkedin_url"),
        now_str(),
    ))
    conn.commit()
    conn.close()


def update_session_from_user(user, **changes):
    session = get_session(user.id) or empty_session(user.id)
    session["username"] = user.username
    session["first_name"] = user.first_name
    for key, value in changes.items():
        session[key] = value
    session["updated_at"] = now_str()
    save_session_data(session)
    return session


