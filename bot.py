    if state == "awaiting_access":
        return f"У вас не завершён старт по специализации: {profession}."
    if state == "testing":
        return f"У вас не завершён тест по специализации: {profession}."
    if state == "awaiting_full_name":
        return f"Вы уже завершили вопросы по специализации: {profession}. Осталось указать ФИО."
    if state == "awaiting_linkedin":
        return f"Вы уже указали ФИО для специализации: {profession}. Осталось отправить LinkedIn."
    return RESUME_TEXT


